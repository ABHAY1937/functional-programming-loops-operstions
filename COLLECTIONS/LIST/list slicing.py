#LIST SLICING

lst=[2,2,3,4,54,6,756,7,854,3,6,7,88,532]

#slicing
#[star:stop] stop-1

print(lst[3:8]) # index3 to 7 vera print cheyum
print(lst[9:12])
print(lst[4:6])
print(lst[3:]) #index3 to end of the list
print(lst[6:])
print(lst[:8]) #0 to 7 values
print(lst[:3])
#
print(lst[:]) #====>full list generate cheyum

#step function
#[start:stop:step]
print(lst[3:9:2])  #here 2 is the increment value
print(lst[2:7:2])
print(lst[5:9:3])

print(lst[4::2])
print(len(lst))

print(lst[:10:3])

print(lst[::5])

